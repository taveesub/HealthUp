CREATE TABLE person(
    person_no character varying(10),
    name character varying(100),
    age integer,
    gender character varying(10),
    height integer,
    weight integer,
    PRIMARY KEY(person_no)

);

CREATE TABLE sport (
    code character varying(10),
    name character varying(100),
    group0 boolean,
    group1 boolean,
    group2 boolean,
    group3 boolean,
    group4 boolean,
    group5 boolean,
    guide integer,
    PRIMARY KEY(code)

);

